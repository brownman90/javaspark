package gs.nick;

class MyData {
	private String name;
	private String msg;
	private int age;

	public void setName(String n) {
		name = n;
	}

	public String getName() {
		return name;
	}

	public void setMsg(String c) {
		msg = c;
	}

	public String getMsg() {
		return msg;
	}

	public void setAge(int a) {
		age = a;
	}

	public int getAge() {
		return age;
	}
}